const COLOR_TERM_PATTERNS: &[&str] = &[
    "xterm", "alacritty", "kitty", "wezterm", "tmux", "screen",
    "256", "gnome", "konsole", "rxvt",
];

fn color_capable() -> bool {
    if let Ok(v) = std::env::var("NO_COLOR") {
        if !v.is_empty() {
            return false;
        }
    }
    let term = std::env::var("TERM").unwrap_or_default();
    if term.is_empty() || term == "dumb" {
        return false;
    }
    let colorterm = std::env::var("COLORTERM").unwrap_or_default().to_lowercase();
    if colorterm.contains("truecolor") || colorterm.contains("24bit") {
        return true;
    }
    let term_lc = term.to_lowercase();
    for p in COLOR_TERM_PATTERNS {
        if term_lc.contains(p) {
            return true;
        }
    }
    if term_lc.starts_with("st") {
        return true;
    }
    false
}

// Generated by build.rs from src/statusline-providers/*.lua
